app.post("/po/update/:number", (req, res) => {
  const index = req.params.number;
  if (pokemon[index]) {
    pokemon[index].id = req.body.id;
    pokemon[index].name = req.body.name;
    pokemon[index].type = req.body.type;
    pokemon[index].class = req.body.class;
    pokemon[index].character = req.body.character;
    pokemon[index].region = req.body.region;
  }
  res.redirect('/po');
});


