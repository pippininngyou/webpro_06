app.post("/po", (req, res) => {
  const newId = pokemon.length > 0 ? pokemon[pokemon.length - 1].id + 1 : 1;
  const newPokemon = {
    id: newId,
    name: req.body.name,
    type: req.body.type,
    class: req.body.class, 
    character: req.body.class,
    region: req.body.region 
  };
  apple.push(newPokemon);
  res.redirect('/po');
});

